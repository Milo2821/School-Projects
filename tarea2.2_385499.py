# Solicitar al usuario 3 valores numéricos enteros
valor1 = int(input("Ingrese el primer valor entero: "))
valor2 = int(input("Ingrese el segundo valor entero: "))
valor3 = int(input("Ingrese el tercer valor entero: "))

# Realizar la suma de los 3 valores
suma = valor1 + valor2 + valor3
print(f"La suma de los valores es: {suma}")

# Realizar la multiplicación de los 3 valores
multiplicacion = valor1 * valor2 * valor3
print(f"La multiplicación de los valores es: {multiplicacion}")

# Realizar la resta de los 3 valores
resta = valor1 - valor2 - valor3
print(f"La resta de los valores es: {resta}")